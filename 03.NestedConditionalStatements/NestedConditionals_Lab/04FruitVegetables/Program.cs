using System;
    class Program
    {
        static void Main()
        {
        string a = Console.ReadLine();

        if (a == "banana" || a == "apple" || a == "kiwi" || a == "cherry" || a == "lemon" || a == "grapes")
        {
            Console.WriteLine("fruit");
        }
        else if (a == "tomato" || a == "cucumber" || a == "pepper" || a == "carrot")
        {
            Console.WriteLine("vegetable");
        }
        else
        {
            Console.WriteLine("unknown");
        }
        }
    }


